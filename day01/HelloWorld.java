public class HelloWorld {
    // 主方法，程序的入口
    public static void main(String[] args) {
        // 输出字符串"Hello, World!"到控制台
        System.out.println("Hello, World!");
    }

    //打印3行HelloWorld
    public static void printHelloWorld() {
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
    }
}